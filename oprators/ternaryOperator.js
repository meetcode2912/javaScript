let age = 18;
let msg = (age >= 18) ? "Adult" : "Minor";
console.log(msg); // Adult

// Hindi Explanation:
// Ternary operator ek shorthand if-else statement hai.
// Syntax: condition ? value_if_true : value_if_false
// Agar condition true hai to value_if_true return hota hai, warna value_if_false return hota hai.
// Note: Ternary operator ka result ek value hota hai, jo kisi variable me store kiya ja sakta hai.
