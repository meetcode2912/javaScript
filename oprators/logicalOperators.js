let a = true, b = false;
console.log(a && b); // AND → false
console.log(a || b); // OR → true
console.log(!a);     // NOT → false
console.log(!b);     // NOT → true
console.log(a != b); // NOT EQUAL → true

// Hindi Explanation:
// && (AND) dono conditions true honi chahiye
// || (OR) kam se kam ek condition true honi chahiye
// ! (NOT) condition ko ulta kar deta hai
// != (NOT EQUAL) do values barabar nahi hain to true return karta hai
// Note: Logical operators ka result hamesha boolean (true/false) hota hai.
