let num = 5;
console.log(num++); // 5 (pehle print fir +1)
console.log(num);   // 6

let n = 5;
console.log(++n);  // 6 (pehle +1 fir print)
console.log(n);    // 6

let val = 5;
console.log(val--); // 5 (pehle print fir -1)
console.log(val);   // 4

let v = 5;
console.log(--v);  // 4 (pehle -1 fir print)
console.log(v);    // 4

// Hindi Explanation:
// Increment operator (++) value ko 1 se badhata hai.
// Decrement operator (--) value ko 1 se ghatata hai.
// Prefix form (++x ya --x) pehle value ko badhata/ghatata hai, fir use karta hai.
// Postfix form (x++ ya x--) pehle value ko use karta hai, fir badhata/ghatata hai.
// Note: Increment/Decrement operators sirf numbers par hi kaam karte hain.
