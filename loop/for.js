for (let i = 1; i <= 5; i++) {
    console.log(i);
}

// Output: 1 2 3 4 5
// Note: for loop me initialization, condition, aur increment/decrement ek hi line me likhe jate hain.
// Hindi Explanation:
// for loop ek aisa loop hai jo tab tak chalta hai jab tak condition true hoti hai.

/*  
  Syntax: 
   for (initialization; condition; increment/decrement)
    { // code to be executed } 
*/

// Initialization: loop variable ko initialize karta hai (jaise let i = 1).
// Condition: ye check karta hai ki loop kab tak chalega (jaise i <= 5).
// Increment/Decrement: ye loop variable ko update karta hai har iteration ke baad (jaise i++).
// Loop ke andar ka code tab tak execute hota rahega jab tak condition true rahegi.